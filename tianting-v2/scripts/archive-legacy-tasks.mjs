#!/usr/bin/env node
/**
 * archive-legacy-tasks.mjs
 * Move legacy *.task.md files into tasks/archive/legacy/ and update README list.
 */
import { promises as fs } from 'fs';
import path from 'path';

const ROOT = path.resolve(process.cwd());
const LEGACY_DIR = path.join(ROOT, 'tasks', 'archive', 'legacy');
const TARGET_README = path.join(LEGACY_DIR, 'README.md');

async function scanAndMove(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      // skip archive dir itself
      if (!fullPath.includes(path.join('tasks', 'archive', 'legacy'))) {
        await scanAndMove(fullPath);
      }
    } else if (entry.isFile() && entry.name.endsWith('.task.md')) {
      const destPath = path.join(LEGACY_DIR, entry.name);
      await fs.mkdir(LEGACY_DIR, { recursive: true });
      await fs.copyFile(fullPath, destPath);
    }
  }
}

async function updateReadme() {
  const files = await fs.readdir(LEGACY_DIR);
  const list = files.filter(f => f.endsWith('.task.md')).sort();
  const lines = [
    '# Legacy Task Archive',
    '',
    '> 以下列表由脚本自动生成，记录已归档的 v1 时代任务卡，仅供参考。',
    '',
    ...list.map((f, i) => `- [${f}](./${f})`),
    '',
    '_Generated by archive-legacy-tasks.mjs_',
  ].join('\n');
  await fs.writeFile(TARGET_README, lines, 'utf-8');
}

(async () => {
  try {
    console.log('Scanning repository for legacy tasks...');
    await scanAndMove(ROOT);
    console.log('Updating README list...');
    await updateReadme();
    console.log('Archive complete ✅');
  } catch (err) {
    console.error('Archive failed:', err);
    process.exit(1);
  }
})(); 